async function seed() {
  const sql = neon(DATABASE_URL!);
  const db = drizzle(sql);

  console.log(`Seeding ${PURCHASES.length} MSTR BTC purchases...`);

  for (const [num, date, btc, avgCost, costM, cumBtc, adso] of PURCHASES) {
    await db.insert(mstrBtcPurchases).values({
      purchaseNumber: num,
      reportDate: date,
      btcAcquired: btc,
      avgBtcCost: avgCost.toString(),
      acquisitionCostUsd: (costM * 1_000_000).toString(),
      cumulativeBtc: cumBtc,
      adsoThousands: adso,
    }).onConflictDoNothing();
  }

  console.log("Done! Seeded all MSTR BTC purchases.");
}

seed().catch((e) => {
  console.error("Seed failed:", e);
  process.exit(1);
});
